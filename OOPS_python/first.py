# how to create class
class Country:  # syntax: class class_name: pass
    pass  # pass represents the empty class


# how to create objects
# Syntax: Object_name=class_name()
india = Country()
usa = Country()

# how to create data of an object
# object_name.data_name = value
india.name = "India"
india.capital = "New Delhi"
usa.name = "USA"
usa.capital = "Washington"
print(india.name)
print(india.capital)
print(usa.name)
print(usa.capital)
